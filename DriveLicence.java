public class DriveLicence{
	String licence_no;
	String sin;
	String class;
	//must be blob variable for photo
	String issuing_date;
	String expiring_date;

	public DriveLicence(String licence_no, String sin, String class, String issuing_date, String expiring_date){
		this.licence_no = licence_no;
		this.sin = sin;
		this.class = class;
		String.issuing_date = issuing_date;
		String.expiring_date = expiring_date;
	}
}